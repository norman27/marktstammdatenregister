// Area Chart Example
var ctx = document.getElementById("netPowerChart");
var myLineChart = new Chart(ctx, {
  type: 'line',
  data: {
    labels: netPowerChartLabels,
    datasets: [
      {
        label: "Net Power",
        borderColor: "blue",
        borderWidth: 1,
        lineTension: 0,
        data: netPowerChartValues,
      }
    ],
  },
  options: {
    maintainAspectRatio: false,
    layout: {
      padding: {
        left: 10,
        right: 25,
        top: 25,
        bottom: 0
      }
    },
    scales: {
      xAxes: [{
        type: 'time',
        time: {
          unit: 'day'
        },
        gridLines: {
          display: false,
          drawBorder: false
        },
        ticks: {
          maxTicksLimit: 7
        }
      }],
      yAxes: [{
        ticks: {
          maxTicksLimit: 5,
          padding: 10,
          callback: function(value, index, values) {
            return value + ' kW';
          }
        },
      }],
    },
    legend: {
      display: false
    }
  }
});

